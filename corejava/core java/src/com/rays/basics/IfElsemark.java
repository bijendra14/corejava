package com.rays.basics;

public class IfElsemark {
	public static void main(String[] args) {
		int marks =79;
		if (marks>=90) {
			System.out.println("Grade A");
		}
		else if (marks>=80){
			System.out.println("grade B");
			
		}
		else if(marks>=70) {
			System.out.println("Grade C");
		}
		else {
			System.out.println("Grade D");
		}
	}
}
		
	

